def _safe_meta(
    meta: dict[str, Any], _visited: set[int] | None = None
) -> dict[str, Any]:
    """Best-effort JSON-safe coercion for level/PA meta dicts.

    A ``_visited`` set guards against self-referential or cyclic dicts; the
    EA parser is defensive but a hand-crafted ``lines_*.json`` could in
    theory smuggle one in, and infinite recursion here would crash the WS
    broadcaster for every connected client.
    """
    if _visited is None:
        _visited = set()
    out: dict[str, Any] = {}
    for k, v in meta.items():
        if v is None or isinstance(v, (bool, int, str)):
            out[k] = v
        elif isinstance(v, float):
            out[k] = _opt_float(v)
        elif isinstance(v, (list, tuple)):
            out[k] = list(v)  # ISO-time strings & numbers pass through
        elif isinstance(v, dict):
            if id(v) in _visited:
                out[k] = "<cycle>"
                continue
            _visited.add(id(v))
            out[k] = _safe_meta(v, _visited)
            _visited.discard(id(v))
        else:
            out[k] = str(v)
    return out


def snapshot_to_json(state: LatestState, include_baseline: bool = True) -> dict[str, Any]:
    """Render the entire :class:`LatestState` into a flat JSON-ready dict.

    *include_baseline* controls whether the static 2 MB ``oos_baseline`` is
    embedded. It never changes for the process lifetime, so the broadcaster
    ships it only in the FIRST full snapshot per connection and passes
    ``include_baseline=False`` on every recurring full — the client caches the
    first copy. Default ``True`` keeps the function self-contained for tests
    and the smoke verifier.
    """
    snap = state.snapshot()
    out: dict[str, Any] = {
        "version": int(snap["version"]),  # type: ignore[arg-type]
        "ts": float(snap["ts"]),  # type: ignore[arg-type]
        "status": serialize_status(snap["status"]),  # type: ignore[arg-type]
        "price": serialize_price(snap["price"]),  # type: ignore[arg-type]
        "analysis": serialize_analysis(snap["analysis"]),  # type: ignore[arg-type]
        "account": serialize_account(snap["account"]),  # type: ignore[arg-type]
        "structures": serialize_structures(snap["structures"]),  # type: ignore[arg-type]
        "performance": serialize_performance(snap["performance"]),  # type: ignore[arg-type]
        "calendar": serialize_calendar(snap["calendar"]),  # type: ignore[arg-type]
        "validation": serialize_validation(snap["validation"]),  # type: ignore[arg-type]
        "macro": serialize_macro(snap["macro"]),  # type: ignore[arg-type]
        "real_yield": serialize_real_yield(snap["real_yield"]),  # type: ignore[arg-type]
        "validation_history": snap.get("validation_history") or {},
        # Persistent live trigger history (per broker): complete year-bucketed
        # record accumulated on disk, so the dashboard shows every live year in
        # full — not just the broker's sliding window. ``server`` names the
        # broker the record belongs to (shown in the UI).
        "live_history": {
            "server": snap.get("live_trigger_server"),
            "by_symbol": snap.get("live_trigger_history") or {},
        },
        "symbol_order": [s.base for s in config.SYMBOLS],
        "symbol_meta": {
            s.base: {
                "display_size": s.display_size,
                "round_step": s.round_step,
                # Pips-conversion meta (config-driven, connection-independent):
                # oos_point = the frozen baseline's price/point; pip_price = the
                # market pip in price units. Lets the frontend render net P/L in
                # PIPS for BOTH the 16Y baseline and the live feed consistently.
                **(
                    {"oos_point": config.OOS_BASELINE_POINT[s.base],
                     "pip_price": config.PIP_PRICE[s.base]}
                    if s.base in config.OOS_BASELINE_POINT
                    and s.base in config.PIP_PRICE else {}
                ),
                # Broker-provided numeric meta (filled in once at startup) —
                # used by the frontend to compute pips/spread in broker units
                # instead of guessing from price magnitude.
                **(snap.get("broker_meta", {}) or {}).get(s.base, {}),
            }
            for s in config.SYMBOLS
        },
    }
    # Static 16-y OOS baseline: ~2 MB and constant for the whole process. Ship
    # it only on the first full snapshot per connection (the client caches it),
    # so it is not re-serialised + re-sent on every analysis tick + heartbeat.
    if include_baseline:
        out["oos_baseline"] = load_oos_baseline()
    return out


def snapshot_light(state: LatestState) -> dict[str, Any]:
    """A price-only update: status / price / account, no heavy analysis blocks.

    Sent at the 2 Hz price cadence so the ~169 KB analysis payload is not
    re-shipped every tick. ``partial: True`` tells the client to MERGE this
    into the snapshot it already holds rather than replace it.

    Uses :meth:`LatestState.light_snapshot` (not the full ``snapshot``) so the
    ever-growing live-trigger history is not deep-copied twice a second for
    fields this payload never includes.
    """
    snap = state.light_snapshot()
