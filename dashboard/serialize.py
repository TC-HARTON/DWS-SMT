# 16-year offline OOS baseline, generated by scripts/_generate_oos_baseline.py
# and shipped with the project. Loaded once on first call so the dashboard
# pays the file-read cost at most once per process. An absent / malformed
# file degrades to an empty payload — the front end then simply hides the
# "16y reference" line.
_OOS_BASELINE_CACHE: dict[str, Any] | None = None


def load_oos_baseline() -> dict[str, Any]:
    """Read and cache the offline OOS-baseline JSON (idempotent)."""
    global _OOS_BASELINE_CACHE
    if _OOS_BASELINE_CACHE is not None:
        return _OOS_BASELINE_CACHE
    path = Path(__file__).resolve().parent.parent / "data" / "oos_baseline.json"
    if not path.exists():
        _log.info("oos_baseline.json missing at %s — feature degraded", path)
        _OOS_BASELINE_CACHE = {}
        return _OOS_BASELINE_CACHE
    try:
        _OOS_BASELINE_CACHE = json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError) as exc:
        _log.warning("oos_baseline.json unreadable (%s) — feature degraded", exc)
        _OOS_BASELINE_CACHE = {}
    return _OOS_BASELINE_CACHE
